package demos;

import com.fasterxml.jackson.annotation.JsonProperty;

public class Person {
    @JsonProperty
    private String name;
    private String sex;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
